package ex02;

public class LgTv {

	public void turnOn() {
		System.out.println("LgTv.. 전원 키다.");
	}
	
	public void turnOff() {
		System.out.println("LgTv.. 전원 끈다.");
	}
	
	public void soundUp() {
		System.out.println("LgTv.. 소리를 올리다.");
	}
	
	public void soundDown() {
		System.out.println("LgTv.. 소리 내리다.");
	}
	
}
